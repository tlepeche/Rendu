char *ft_strcapitalize(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[0] <= 'z' && str[0] >= 'a')
			str[0] -= 32;
		if (((str[a - 1] >= 1) && (str [a - 1] <= 47)) ||
				((str[a - 1] >= 58) && (str[a - 1] <= 64)) ||
				((str[a - 1] >= 91) && (str[a - 1] <= 96)) ||
				(str[a - 1] >= 123))
		{
			if ((str[a] <= 'z') && (str[a] >= 'a'))
				str[a] -= 32;
		}
		if (str[a] <= 'Z' && str[a] >= 'A')
		{
			if (((str[a - 1] > 47) && (str[a - 1] <= 57)) ||
					((str[a - 1] >= 65) && (str[a - 1] <= 90)) ||
					((str[a - 1] >= 97) && (str[a - 1] <= 122)))
				str[a] += 32;
		}
		a++;
	}
	return ((char*)str);
}
