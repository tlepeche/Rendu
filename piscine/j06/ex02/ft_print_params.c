#include <unistd.h>

void	ft_putchar(char c);

void	ft_print_params(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		ft_putchar(str[a]);
		a++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = 1;
	
	while (i < argc)
	{
		ft_print_params(argv[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
